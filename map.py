class Map:
    def __init__(self,game):
        self.game = game
        self.map_layout = map_layout
        self.map = {}
        self.get_map()

    
    def get_map(self):
        for j, row in enumerate(self.map_layout):
            for i, value in enumerate(row):
                if value:
                    self.map[(i,j)] = value

    
    def draw(self):
        [pg.draw.rect(self.game.screen, 'darkgray', (pos[0]*50, pos[1] * 50, 50, 50),2)
        for pos in self.map]
